import random

# Процедура замены первого и последнего элементов
def swap_first_last(arr):
    if len(arr) < 2:
        return
    arr[0], arr[-1] = arr[-1], arr[0]

# Ввод длины массива
m = int(input("Введите количество элементов массива: "))

if m <= 0:
    print("Ошибка: количество элементов должно быть больше 0")
else:
    # Ввод диапазона
    a = int(input("Введите минимальное значение диапазона: "))
    b = int(input("Введите максимальное значение диапазона: "))

    if a > b:
        print("Ошибка: минимальное значение больше максимального")
    else:
        A = []

        # Генерация массива
        for i in range(m):
            A.append(random.randint(a, b))

        # Вывод исходного массива
        print("Исходный массив:", A)

        # Замена первого и последнего элементов
        swap_first_last(A)

        # Вывод измененного массива
        print("Измененный массив:", A)

input("Нажмите Enter, чтобы закрыть...")
import math

# Функция для вычисления площади по формуле Герона
def heron(a, b, c):
    p = (a + b + c) / 2
    s = math.sqrt(p * (p - a) * (p - b) * (p - c))
    return s

# Ввод данных для первого треугольника
a1 = float(input("Введите сторону a1: "))
b1 = float(input("Введите сторону b1: "))
c1 = float(input("Введите сторону c1: "))

# Ввод данных для второго треугольника
a2 = float(input("Введите сторону a2: "))
b2 = float(input("Введите сторону b2: "))
c2 = float(input("Введите сторону c2: "))

# Ввод данных для третьего треугольника
a3 = float(input("Введите сторону a3: "))
b3 = float(input("Введите сторону b3: "))
c3 = float(input("Введите сторону c3: "))

# Вычисление площадей
s1 = heron(a1, b1, c1)
s2 = heron(a2, b2, c2)
s3 = heron(a3, b3, c3)

print("Площадь первого треугольника:", s1)
print("Площадь второго треугольника:", s2)
print("Площадь третьего треугольника:", s3)

# Проверка равновеликости
if abs(s1 - s2) < 0.0001 and abs(s2 - s3) < 0.0001:
    print("Все три треугольника равновеликие.")
else:
    print("Треугольники не равновеликие.")

